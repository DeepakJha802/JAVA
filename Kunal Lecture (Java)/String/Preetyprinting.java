public class Preetyprinting {

    public static void main(String[] args) {

        float a = 450.23654f;
        System.out.printf("Formatted number is %.2f",a);

        System.out.println();

        System.out.printf("Pie : %.2f", Math.PI);    // Math is a class. 
    }

}
// Format specifier is use with printf method.
